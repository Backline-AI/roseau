package com.github.maracas.roseau.model;

/**
 * Java top-level declaration kinds
 */
public enum DeclarationKind {
	/**
	 * Class type
	 */
	CLASS,

	/**
	 * Interface type
	 */
	INTERFACE,

	/**
	 * Record type
	 */
	RECORD,

	/**
	 * Enum type
	 */
	ENUM,

	/**
	 * Annotation type
	 */
	ANNOTATION
}
